def _has_audit_schema(path: Path) -> bool:
    if not path.exists():
        return False
    import sqlite3

    try:
        with sqlite3.connect(path) as conn:
            row = conn.execute(
                "SELECT 1 FROM sqlite_master WHERE type='table' AND name='audit_trail'"
            ).fetchone()
            return row is not None
    except sqlite3.Error:
        return False


def get_db_path() -> Path:
    if _has_audit_schema(REQUESTED_DB_PATH):
        return REQUESTED_DB_PATH
    return FALLBACK_DB_PATH


def _parse_timestamp(value: str | None) -> datetime | None:
    if not value:
        return None
    for fmt in ("%Y-%m-%d %H:%M:%S.%f", "%Y-%m-%d %H:%M:%S"):
        try:
            return datetime.strptime(value, fmt)
        except ValueError:
            continue
    try:
        return datetime.fromisoformat(value)
    except ValueError:
        return None


def _sessionize(rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    rows = sorted(rows, key=lambda row: row.get("timestamp") or "")
    last_agent = ""
    last_ts: datetime | None = None
    counter = 0
    current_session = ""

    for row in rows:
        agent_id = row.get("agent_id") or "unknown-agent"
        ts = _parse_timestamp(row.get("timestamp"))
        same_session = (
            bool(current_session)
            and agent_id == last_agent
            and ts is not None
            and last_ts is not None
            and ts - last_ts < timedelta(seconds=30)
        )
        if not same_session:
            counter += 1
            current_session = f"{agent_id}-session-{counter:04d}"
        row["session_id"] = current_session
        row["trace_id"] = None
        last_agent = agent_id
        last_ts = ts
    return rows


def _enrich_with_chain_data(rows: list[dict[str, Any]]) -> None:
    """Follow hash chains to assign trace_ids and compute latency_ms on terminal events."""
    by_prev_hash: dict[str, dict[str, Any]] = {}
    for row in rows:
        ph = row.get("prev_hash")
        if ph:
            by_prev_hash[ph] = row

    for row in rows:
        if row.get("outcome") != "INTERCEPTOR_START":
            continue
        start_ts = _parse_timestamp(row.get("timestamp"))
        trace_id = f"trace-{(row.get('hash') or '')[:12]}"
        row["trace_id"] = trace_id

        current = row
        for _ in range(30):
            h = current.get("hash")
            if not h:
                break
            nxt = by_prev_hash.get(h)
            if not nxt:
                break
            nxt["trace_id"] = trace_id
            if nxt.get("outcome") in TERMINAL_OUTCOMES:
                if start_ts:
                    end_ts = _parse_timestamp(nxt.get("timestamp"))
                    if end_ts and end_ts >= start_ts:
                        nxt["latency_ms"] = int((end_ts - start_ts).total_seconds() * 1000)
                break
            current = nxt


def _extract_confidence(reason: str | None) -> float | None:
    if not reason:
        return None
    match = re.search(r"confidence[:\s]+([0-9.]+)", reason, re.IGNORECASE)
    if not match:
        return None
    try:
        return float(match.group(1))
    except ValueError:
        return None


def _infer_verdict(outcome: str) -> str:
    if outcome in ALLOW_OUTCOMES:
        return "ALLOW"
    if outcome in BLOCK_OUTCOMES:
        return "DENY"
    if outcome in HITL_OUTCOMES:
        return "HITL"
    if "UNCERTAIN" in outcome:
        return "UNCERTAIN"
    return outcome


def _extract_stage(outcome: str, reason: str | None) -> str:
    if outcome in INTERMEDIATE_STAGE:
        return INTERMEDIATE_STAGE[outcome]
    if outcome == "OUTPUT_BLOCK":
        return "Output Guard"
    r = (reason or "").lower()
    if "deberta" in r or "stage 2.5" in r:
        return "Stage 2.5 DeBERTa"
    if "stage 3" in r or "llm" in r or "double-check" in r or "double_check" in r:
        return "Stage 3 LLM"
    if "stage 2" in r:
        return "Stage 2"
    if "stage 1" in r or "regex" in r:
        return "Stage 1"
    if "not in permissions" in r or "suspended" in r or "canary" in r or "l1.5" in r:
        return "L1.5"
    return INTERMEDIATE_STAGE.get(outcome, "Unknown")


def _infer_security_model(outcome: str, reason: str | None) -> str:
    text = f"{outcome} {reason or ''}".lower()
    if "stage 2.5b" in text or "prompt guard" in text:
        return "Prompt Guard / ProtectAI fallback"
    if "deberta" in text or "stage_2.5" in text or "stage 2.5" in text:
        return "DeBERTa Stage 2.5"
    if "gemma" in text or "stage 3" in text or "llm" in text:
        return "Stage 3 Gemma 2B"
    if "stage 2" in text or "classifier" in text:
        return "TF-IDF + Random Forest"
    if "stage 1" in text or "regex" in text:
        return "Stage 1 Regex"
    return "L1.5 / policy gate"


def _terminal_trace_ids(rows: list[dict[str, Any]], outcomes: set[str] | None = None) -> set[str]:
    trace_ids: set[str] = set()
    for row in rows:
        outcome = row.get("outcome") or ""
        if outcome not in TERMINAL_OUTCOMES:
            continue
        if outcomes is not None and outcome not in outcomes:
            continue
        trace_id = row.get("trace_id")
        if trace_id:
            trace_ids.add(trace_id)
    return trace_ids


def _normalize_event(row: dict[str, Any]) -> AuditEvent:
    outcome = row.get("outcome") or ""
    reason = row.get("reason") or ""
    article, control = ARTICLE_BY_OUTCOME.get(outcome, (None, None))
    return AuditEvent(
        event_id=row.get("hash") or "",
        timestamp=str(row.get("timestamp") or ""),
        agent_id=row.get("agent_id") or "",
        action=row.get("action") or "",
        outcome=outcome,
        reason=reason,
        trace_id=row.get("trace_id"),
        session_id=row.get("session_id"),
        latency_ms=row.get("latency_ms"),
        confidence=_extract_confidence(reason),
        eu_ai_act_article=article,
        eu_ai_act_control=control,
        tool_name=row.get("action") or None,
        stage=_extract_stage(outcome, reason),
        verdict=_infer_verdict(outcome),
        agent_model=None,
        security_model=_infer_security_model(outcome, reason),
        prev_hash=row.get("prev_hash"),
    )


async def _fetch_rows() -> list[dict[str, Any]]:
    db_path = get_db_path()
    if not _has_audit_schema(db_path):
        return []
    async with aiosqlite.connect(db_path) as conn:
        conn.row_factory = aiosqlite.Row
        cursor = await conn.execute(
            "SELECT hash, timestamp, agent_id, action, outcome, reason, prev_hash "
            "FROM audit_trail ORDER BY timestamp ASC"
        )
        rows = [dict(row) for row in await cursor.fetchall()]
    rows = _sessionize(rows)
    _enrich_with_chain_data(rows)
    return rows


async def get_recent_events(limit: int = 100) -> list[AuditEvent]:
    rows = await _fetch_rows()
    return [_normalize_event(row) for row in rows[-limit:]][::-1]


async def get_session_events(session_id: str) -> list[AuditEvent]:
    rows = await _fetch_rows()
    session_rows = [row for row in rows if row.get("session_id") == session_id]
    terminal = [row for row in session_rows if (row.get("outcome") or "") in TERMINAL_OUTCOMES]
    source = terminal if terminal else session_rows
    return [_normalize_event(row) for row in source]


async def get_trace_events(trace_id: str) -> list[AuditEvent]:
    rows = await _fetch_rows()
    return [_normalize_event(row) for row in rows if row.get("trace_id") == trace_id]


async def get_sessions(
    limit: int = 50,
    agent_id: str | None = None,
    show_eval: bool = False,
) -> list[SessionSummary]:
    rows = await _fetch_rows()
    sessions: dict[str, list[dict[str, Any]]] = {}
    for row in rows:
        if agent_id and row.get("agent_id") != agent_id:
            continue
        if not show_eval and row.get("agent_id") in EVAL_TOOL_AGENTS:
            continue
        sessions.setdefault(row["session_id"], []).append(row)

    summaries = []
    for session_id, group in sessions.items():
        ordered = sorted(group, key=lambda row: row.get("timestamp") or "")
        start = _parse_timestamp(ordered[0].get("timestamp"))
        end = _parse_timestamp(ordered[-1].get("timestamp"))
        duration_ms = int((end - start).total_seconds() * 1000) if start and end else 0
        terminal_trace_ids = _terminal_trace_ids(ordered)
        blocked_trace_ids = _terminal_trace_ids(ordered, BLOCK_OUTCOMES)
        allowed_trace_ids = _terminal_trace_ids(ordered, ALLOW_OUTCOMES)
        hitl_trace_ids = _terminal_trace_ids(ordered, HITL_OUTCOMES)
        summaries.append(SessionSummary(
            session_id=session_id,
            agent_id=ordered[0].get("agent_id") or "",
            start_time=str(ordered[0].get("timestamp") or ""),
            end_time=str(ordered[-1].get("timestamp") or ""),
            total_events=len(terminal_trace_ids),
            blocked_count=len(blocked_trace_ids),
            allowed_count=len(allowed_trace_ids),
            hitl_count=len(hitl_trace_ids),
            duration_ms=duration_ms,
        ))
    summaries.sort(key=lambda item: item.start_time, reverse=True)
    return summaries[:limit]


async def get_metrics(agent_id: str | None = None) -> KPIMetrics:
    rows = await _fetch_rows()
    if agent_id:
        rows = [row for row in rows if row.get("agent_id") == agent_id]

    # Count unique tool-call interceptions — one per trace_id, not one per pipeline row
    total_tool_calls = len(set(row.get("trace_id") for row in rows if row.get("trace_id")))

    terminal_rows = [row for row in rows if (row.get("outcome") or "") in TERMINAL_OUTCOMES]
    blocked = sum(1 for row in terminal_rows if (row.get("outcome") or "") in BLOCK_OUTCOMES)
    allowed = sum(1 for row in terminal_rows if (row.get("outcome") or "") in ALLOW_OUTCOMES)
    hitl = sum(1 for row in terminal_rows if (row.get("outcome") or "") in HITL_OUTCOMES)
    terminal = blocked + allowed + hitl

    latencies = [row["latency_ms"] for row in terminal_rows if row.get("latency_ms") is not None]
    avg_latency_ms = sum(latencies) / len(latencies) if latencies else 0.0
    p95_latency_ms = 0.0
    if latencies:
        s = sorted(latencies)
        idx = max(0, int(0.95 * len(s)) - 1)
        p95_latency_ms = float(s[idx])

    now = datetime.utcnow()
    cutoff = now - timedelta(hours=24)
    # Count unique traces seen in last 24 h (not raw rows)
    tool_calls_last_24h = len(set(
        row.get("trace_id") for row in rows
        if row.get("trace_id")
        and (_parse_timestamp(row.get("timestamp")) or datetime.min) >= cutoff
    ))

    # Dataset age — needed to compute a meaningful trend
    timestamps = [
        ts for ts in (_parse_timestamp(row.get("timestamp")) for row in rows)
        if ts is not None
    ]
    calls_trend_pct: float | None = None
    if timestamps and total_tool_calls > 0:
        dataset_days = max(1, (now - min(timestamps)).days)
        if dataset_days > 1:
            daily_avg = total_tool_calls / dataset_days
            if daily_avg > 0:
                calls_trend_pct = round((tool_calls_last_24h - daily_avg) / daily_avg * 100, 1)

    return KPIMetrics(
        total_tool_calls=total_tool_calls,
        detection_rate=(blocked / terminal) if terminal else 0.0,
        false_positive_rate=0.0,
        blocked_count=blocked,
        allowed_count=allowed,
        hitl_count=hitl,
        avg_latency_ms=round(avg_latency_ms, 1),
        p95_latency_ms=round(p95_latency_ms, 1),
        tool_calls_last_24h=tool_calls_last_24h,
        calls_trend_pct=calls_trend_pct,
    )


async def get_agents(show_eval: bool = False) -> list[str]:
    db_path = get_db_path()
    if not _has_audit_schema(db_path):
        return []
    async with aiosqlite.connect(db_path) as conn:
        query = (
            "SELECT DISTINCT agent_id FROM audit_trail "
            "WHERE agent_id IS NOT NULL"
        )
        params: list[str] = []
        if not show_eval:
            placeholders = ",".join("?" for _ in EVAL_TOOL_AGENTS)
            query += f" AND agent_id NOT IN ({placeholders})"
            params.extend(sorted(EVAL_TOOL_AGENTS))
        query += " ORDER BY agent_id"
        cursor = await conn.execute(query, params)
        rows = await cursor.fetchall()
    return [row[0] for row in rows]


async def get_total_event_count() -> int:
    db_path = get_db_path()
    if not _has_audit_schema(db_path):
        return 0
    async with aiosqlite.connect(db_path) as conn:
        cursor = await conn.execute("SELECT COUNT(*) FROM audit_trail")
        row = await cursor.fetchone()
        return row[0] if row else 0


async def get_total_trace_count() -> int:
    """Count distinct intercepted tool calls (one per trace_id, not one per pipeline row)."""
    rows = await _fetch_rows()
    return len(set(row.get("trace_id") for row in rows if row.get("trace_id")))


async def get_compliance_events(article_code: str, limit: int = 20) -> list[AuditEvent]:
    outcomes = ARTICLE_OUTCOMES.get(article_code)
    if outcomes is None:
        return []
    rows = await _fetch_rows()
    if outcomes is _ALL_OUTCOMES:
        filtered = list(rows)
    elif outcomes is _BENCHMARK_EVENTS:
        filtered = [row for row in rows if (row.get("agent_id") or "") in EVAL_TOOL_AGENTS]
    else:
        filtered = [row for row in rows if (row.get("outcome") or "") in outcomes]
    filtered.sort(key=lambda r: r.get("timestamp") or "", reverse=True)
    return [_normalize_event(row) for row in filtered[:limit]]


async def get_hitl_events() -> list[AuditEvent]:
    rows = await _fetch_rows()
    now = datetime.utcnow()
    cutoff = now - timedelta(hours=24)
    hitl_rows = [
        row for row in rows
        if (row.get("outcome") or "") in HITL_OUTCOMES
        and (_parse_timestamp(row.get("timestamp")) or datetime.min) >= cutoff
    ]
    hitl_rows.sort(key=lambda r: r.get("timestamp") or "", reverse=True)
    return [_normalize_event(row) for row in hitl_rows]
